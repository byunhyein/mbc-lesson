// 우항과 좌항의 값을 비교 후 true, false 반환 (조건문에 주로 사용!)

let x = 10;
let y = 20;

console.log(`x>=y : ${x >= y}`); //false
/* x = 10, y = 20. 10은 20보다 크거나 작다 = false. */


console.log(`x>y : ${x > y}`); //false
console.log(`x>=y : ${x >= y}`); //false
console.log(`x<y : ${x < y}`); //true
console.log(`x==y : ${x == y}`) // 같다, false
console.log(`x!==y : ${x !== y}`) // 같지 않다, true
console.log(`x===y : ${x === y}`); //자료형도 같다, false
console.log(`x!=y : ${x != y}`) // 자료형까지 같지 않다. true


